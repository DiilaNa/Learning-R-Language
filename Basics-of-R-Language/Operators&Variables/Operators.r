a <- 8
b <- 3

# Arithmetic Operators

# Addition
sum <- a + b

# Difference
difference <- a - b
# Multiplication
product <- a * b
# Division
division <- a / b
# Modulus
modules <- a %% b
# Exponentiation
power <- a ^ b
print(paste("Sum:", sum))
print(paste("Difference:", difference))
print(paste("Product:", product))
print(paste("Division:", division))   
print(paste("Modulus:", modules))
print(paste("Exponentiation:", power))

# Comparison Operators

# Equal to
equal_to <- (a == b)
# Not equal to
not_equal_to <- (a != b)
# Greater than
greater_than <- (a > b)
# Less than
less_than <- (a < b)
# Greater than or equal to
greater_than_equal_to <- (a >= b)

# Less than or equal to
less_than_equal_to <- (a <= b)

print(paste("Equal to:", equal_to))
print(paste("Not equal to:", not_equal_to))
print(paste("Greater than:", greater_than))
print(paste("Less than:", less_than))
print(paste("Greater than or equal to:", greater_than_equal_to))
print(paste("Less than or equal to:", less_than_equal_to)