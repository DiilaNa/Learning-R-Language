a <- 8
b <- 3

# Arithmetic Operators

# Addition
sum <- a + b

# Difference
difference <- a - b
# Multiplication
product <- a * b
# Division
division <- a / b
# Modulus
modules <- a %% b
# Exponentiation
power <- a ^ b
print(paste("Sum:", sum))
print(paste("Difference:", difference))
print(paste("Product:", product))
print(paste("Division:", division))   
print(paste("Modulus:", modules))
print(paste("Exponentiation:", power))