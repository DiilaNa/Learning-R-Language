# Numeric Vector
numeric_vector <- c(1, 2, 3, 4, 5)
print(numeric_vector)

# Character Vector
char_vector <- c("apple", "banana", "cherry")
print(char_vector)

# Logical Vector
logical_vector <- c(TRUE, FALSE, TRUE, FALSE)
print(logical_vector)

# Sequence Vector
seq_vector <- seq(1, 10, by=2)
seq <- 1:10
print(seq_vector)
print(seq)

# Vector with rep() function
rep_vector <- rep(c(1,2), times=3)
print(rep_vector)